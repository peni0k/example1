import streamlit as st

def main():
    st.title("Анализатор Продаж - Добро пожаловать!")

    st.header("О приложении")
    st.write("""
    Это приложение позволяет анализировать данные о продажах с визуализацией динамики выручки и количества,
    а также прогнозированием будущих значений. Приложение поддерживает фильтрацию по категориям и датам.
    """)

    st.header("Требования к файлу данных")
    
    st.subheader("Поддерживаемые форматы")
    st.write("""
    - CSV файлы (.csv)
    - Excel файлы (.xlsx, .xls)
    """)
    
    st.subheader("Обязательные столбцы")
    st.write("""
    Ваш файл должен содержать следующие столбцы:
    
    - `date` - дата в формате YYYY-MM-DD (например: 2023-01-15)
    - `category` - категория товара или услуги (например: 'Электроника', 'Одежда', 'Дом')
    - `price` - цена за единицу товара/услуги (например: 1500.00)
    - `quantity` - количество проданных единиц (например: 2)
    """)
    
    st.subheader("Названия столбцов")
    st.write("""
    Приложение автоматически распознает следующие варианты названий столбцов:
    
    - `date` / `Дата` / `─рЄр` (для даты)
    - `category` / `Категория` / `╩рЄхуюЁш ` (для категории)
    - `price` / `Цена_за_ед` / `╓хэр_чр_хф` (для цены)
    - `quantity` / `Количество` / `╩юышўхёЄтю` (для количества)
    """)
    
    st.subheader("Формат данных")
    st.write("""
    Пример содержимого файла:
    
    ```csv
    date,category,price,quantity
    2023-01-01,Электроника,5990.50,1
    2023-01-01,Одежда,2990.00,2
    2023-01-02,Дом,15000.00,1
    2023-01-03,Электроника,12990.00,1
    ```
    """)
    
    st.subheader("Примечания")
    st.write("""
    - Дата должна быть в формате YYYY-MM-DD
    - Цена и количество должны быть числовыми значениями
    - Категория может быть текстовой строкой
    - Приложение автоматически преобразует русские названия столбцов в английские
    """)
    
    st.header("Функциональность приложения")
    st.write("""
    После загрузки файла вы сможете:
    
    1. Фильтровать данные по датам и категориям
    2. Просматривать KPI: общую выручку, среднюю выручку в день, общее количество проданных единиц
    3. Визуализировать:
       - Динамику выручки по дням
       - Динамику количества продаж по дням
       - Прогноз выручки и количества
       - Анализ по отдельным категориям
    4. Просматривать исходные данные в табличном виде
    """)

if __name__ == "__main__":
    main()